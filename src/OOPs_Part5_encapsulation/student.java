package OOPs_Part5_encapsulation;

public class student {
	
	private String name;
	private int rollno;
	
	public String getName() {
		return name;
	}
	
	public void setName(String Name) {
			name = Name;
	}
	
	public int rollnoGet() {
		return rollno;
	}
	
	public void rollnoSet(int roll) {
		rollno = roll;
	}
}
